<script>
    document.querySelectorAll('.detail-btn').forEach(btn => {
        btn.addEventListener('click', function () {
            const id = this.dataset.id;

            // Simulasi AJAX / data statis
            const data = {
                metode: "Jemput di depan rumah",
                lokasi: "BSU RW 04 - Rungkut",
                tanggal: "22 Apr 2024",
                jenis: "Plastik",
                berat: 5,
                harga: 12500,
                status: "Berhasil"
            };

            // Tampilkan data ke modal
            document.getElementById('metode').textContent = data.metode;
            document.getElementById('lokasi').textContent = data.lokasi;
            document.getElementById('tanggal').textContent = data.tanggal;
            document.getElementById('jenis').textContent = data.jenis;
            document.getElementById('berat').textContent = `${data.berat} kg`;
            document.getElementById('harga').textContent = `Rp${data.harga.toLocaleString()}`;
            document.getElementById('status').textContent = data.status;
            const qrValue = `https://example.com/nota/${id || Math.floor(Math.random() * 10000)}`;

            const qrAPI = `https://api.qrserver.com/v1/create-qr-code/?data=${encodeURIComponent(qrValue)}&size=100x100`;
            document.getElementById('qrImage').src = qrAPI;
            // Tampilkan modal
            document.getElementById('detailModal').style.display = 'block';
        });
    });

    // Tutup modal
    document.querySelector('.close-btn').onclick = function () {
        document.getElementById('detailModal').style.display = 'none';
    };

    // Opsional: klik di luar modal untuk menutup
    window.onclick = function (event) {
        const modal = document.getElementById('detailModal');
        if (event.target === modal) {
            modal.style.display = 'none';
        }
    };
</script>


